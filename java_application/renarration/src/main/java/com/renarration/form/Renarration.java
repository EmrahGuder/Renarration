package com.renarration.form;

public class Renarration {
	private String renarrationText;
	private String renarrationHTML;
	private String fileName;
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getRenarrationText() {
		return renarrationText;
	}
	public void setRenarrationText(String renarrationText) {
		this.renarrationText = renarrationText;
	}
	public String getRenarrationHTML() {
		return renarrationHTML;
	}
	public void setRenarrationHTML(String renarrationHTML) {
		this.renarrationHTML = renarrationHTML;
	}
	

	
}
